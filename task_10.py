# Найти расстояние между двумя точками пространства

x1 = int(input('Введи 1 координату 1 точки: '))
x2 = int(input('Введи 2 координату 1 точки: '))
x3 = int(input('Введи 3 координату 1 точки: '))
y1 = int(input('Введи 1 координату 2 точки: '))
y2 = int(input('Введи 2 координату 2 точки: '))
y3 = int(input('Введи 3 координату 2 точки: '))

print(((y1-x1)**2 + (y2-x2)**2 + (y3-x3)**2)**0.5)